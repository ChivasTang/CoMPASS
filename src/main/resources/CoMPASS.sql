  `MODEL_TYPE` VARCHAR(50) NOT NULL DEFAULT '' COMMENT 'モデルタイプ',
  `USE_AS` VARCHAR(50) NOT NULL DEFAULT '' COMMENT '使用用途',
  `OWNERSHIP` VARCHAR(50) NOT NULL DEFAULT '' COMMENT '所有権',
  `COLOR` VARCHAR(50) NOT NULL DEFAULT '' COMMENT 'body color',
  `QUANTITY` INT NOT NULL DEFAULT '1' COMMENT '数量',
  `CREATED` TIMESTAMP NOT NULL DEFAULT CURRENT_TIMESTAMP COMMENT '作成タイムスタンプ',
