DROP TABLE IF EXISTS `import_car`;
CREATE TABLE `import_car` (
  `ID` BIGINT NOT NULL AUTO_INCREMENT COMMENT '唯一区分',
  `YEAR` INT NOT NULL DEFAULT '2018' COMMENT '年',
  `MONTH` INT NOT NULL DEFAULT '11' COMMENT '月',
  `PROVINCE` VARCHAR(50) NOT NULL DEFAULT '' COMMENT '省',
  `CITY` VARCHAR(50) NOT NULL DEFAULT '' COMMENT '市',
  `COMPANY` VARCHAR(50) NOT NULL DEFAULT '' COMMENT '企業名称',
  `MODEL` VARCHAR(50) NOT NULL DEFAULT '' COMMENT 'モデル名称',
  `MODEL_STYLE` VARCHAR(255) NOT NULL DEFAULT '' COMMENT 'モデルスタイル',
  `DISPLACEMENT` VARCHAR(50) NOT NULL DEFAULT '' COMMENT '排気量',
